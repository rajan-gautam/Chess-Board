import javax.swing.JButton;

public class Square extends JButton {
	
	private int x;
	private int y;
	
	public Square() {
		super();
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	
}
